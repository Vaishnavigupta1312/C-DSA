#include<bits/stdc++.h>
using namespace std;

int main(){
     int a=4;
     int b=6;
    
    //bitwise and(&) or(|) not(~) and xor(^)
    cout<<"a&b = "<<(a&b) <<endl;
    cout<<"a|b = "<<(a|b) <<endl;
    cout<<"a^b = "<<(a^b) <<endl;
    cout<<"~a = "<<(~a) <<endl;
    cout<<"~b = "<<(~b) <<endl;

    //left shift(*2) and right shift (/2)
    cout<<(17>>1)<<endl;
    cout<<(17>>2)<<endl;
    cout<<(19<<1)<<endl;
    cout<<(21<<2)<<endl;
    


}